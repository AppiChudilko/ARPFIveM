        public static int GetShopIdInRadius(Vector3 pos, float radius, int id)
        {
            for (int i = 0; i < ShopList.Length / 6; i++)
            {
                if (Convert.ToInt32(ShopList[i, 2]) != id) continue;
                if (Main.GetDistanceToSquared(pos, new Vector3((float) ShopList[i, 3], (float) ShopList[i, 4], (float)ShopList[i, 5])) < radius)
                    return Convert.ToInt32(ShopList[i, 1]);
            }
            return -1;
        }

        public static Vector3 FindNearest(Vector3 pos)
        {
            var shopPosPrew = new Vector3((float) ShopList[0, 3], (float) ShopList[0, 4], (float)ShopList[0, 5]);
            for (int i = 0; i < ShopList.Length / 6; i++)
            {
                if (Convert.ToInt32(ShopList[i, 2]) != 0) continue;
                var shopPos = new Vector3((float) ShopList[i, 3], (float) ShopList[i, 4], (float)ShopList[i, 5]);
                if(Main.GetDistanceToSquared(shopPos, pos) < Main.GetDistanceToSquared(shopPosPrew, pos))
                    shopPosPrew = shopPos;
            }
            return shopPosPrew;
        }
        
