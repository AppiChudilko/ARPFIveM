        public static void ParkCar(int id, float x, float y, float z, float rot)
        {
            foreach (var item in VehicleInfoGlobalDataList)
            {
                if (item.id != id) continue;
                
                VehicleInfoGlobalDataList[item.VehId].x_park = x;
                VehicleInfoGlobalDataList[item.VehId].y_park = y;
                VehicleInfoGlobalDataList[item.VehId].z_park = z;
                VehicleInfoGlobalDataList[item.VehId].rot_park = rot;
                
                Server.Sync.Data.Set(110000 + item.VehId, "x_park", x);
                Server.Sync.Data.Set(110000 + item.VehId, "y_park", y);
                Server.Sync.Data.Set(110000 + item.VehId, "z_park", z);
                Server.Sync.Data.Set(110000 + item.VehId, "rot_park", rot);
                
                TriggerClientEvent("ARP:UpdateClientVehPark", item.VehId, x, y, z, rot);
                Appi.MySql.ExecuteQuery("UPDATE cars SET x_park = '" + x + "', y_park = '" + y + "', z_park = '" + z + "', rot_park = '" + rot + "' where id = '" + id + "'");
                return;
            }
        }

